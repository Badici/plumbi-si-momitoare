import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const publicDir = path.join(__dirname, "../public");
const outFile = path.join(__dirname, "../src/data/discovered-images.ts");

const LOGO_FILES = new Set(["1.png", "logo-2.png"]);

function main() {
  const files = fs.existsSync(publicDir)
    ? fs.readdirSync(publicDir).filter((f) => f.toLowerCase().endsWith(".png"))
    : [];

  const productPngs = files
    .filter((f) => !LOGO_FILES.has(f))
    .sort((a, b) => a.localeCompare(b, "ro"));

  const contents = `// Auto-generated by scripts/generate-discovered-images.mjs — do not edit by hand.
export const DISCOVERED_PRODUCT_PNGS = ${JSON.stringify(productPngs, null, 2)} as const;

export type DiscoveredProductPng = (typeof DISCOVERED_PRODUCT_PNGS)[number];
`;

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, contents, "utf8");
  process.stdout.write(`Wrote ${productPngs.length} PNG paths → ${path.relative(process.cwd(), outFile)}\n`);
}

main();
